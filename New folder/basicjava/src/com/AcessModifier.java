package com;

class A1{    // public,protected,default,private can be use in same class
	public int a= 10;
	protected int b = 16;
	int c = 45;
	private int d = 55;
		public void meth1() {
			System.out.println( a);
			System.out.println( b);
			System.out.println( c);
			System.out.println( d);
				
		}
	
}

public class AcessModifier {

	public static void main(String[] args) {
		A1 c = new A1();
		//c.meth1();
		
		System.out.println(c.a);
		System.out.println( c.b);
		System.out.println( c.c);
		//System.out.println( c.d); //private can not be use in same package
		
	}

}
